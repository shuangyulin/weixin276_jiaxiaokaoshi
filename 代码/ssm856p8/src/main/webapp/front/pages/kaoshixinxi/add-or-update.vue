		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取
			this.ruleForm.jiaxiaozhanghao = this.user.jiaxiaozhanghao
			this.ro.jiaxiaozhanghao = true;
			this.ruleForm.jiaxiaomingcheng = this.user.jiaxiaomingcheng
