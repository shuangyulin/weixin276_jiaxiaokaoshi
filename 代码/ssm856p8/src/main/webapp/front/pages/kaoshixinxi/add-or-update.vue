				for (var o in obj){
					if(o=='kaoshikemu'){
					this.ruleForm.kaoshikemu = obj[o];
					this.ro.kaoshikemu = true;
					continue;
					}
					if(o=='fengmiantu'){
					this.ruleForm.fengmiantu = obj[o];
					this.ro.fengmiantu = true;
					continue;
					}
					if(o=='kaoshineirong'){
					this.ruleForm.kaoshineirong = obj[o];
					this.ro.kaoshineirong = true;
					continue;
					}
					if(o=='kaoshichangdi'){
