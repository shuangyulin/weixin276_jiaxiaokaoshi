			// 自定义下拉框值
			this.kaoshikemuOptions = "科目二,科目三".split(',')
			// 下拉框
			res = await this.$api.option(`kaoshichangdi`,`kaoshichangdi`,{});
			this.kaoshichangdiOptions = res.data;
			// 下拉框
			res = await this.$api.option(`kaoshichangci`,`kaoshichangci`,{});
			this.kaoshichangciOptions = res.data;

